
# if, elif (else if), and else
    # if: if condition is true, do this
    # elif: if another condition is true, do this
    # else: if nothing above is true, do this

# Demo:

x = 11
if (x > 10):
    print("x is greater than 10")
elif (x == 10):
    print("x is equal to 10")
else:
    print("x is less than 10")